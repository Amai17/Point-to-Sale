    private void cboFormaPagoActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_cboFormaPagoActionPerformed
        // TODO add your handling code here:
        if (cboFormaPago.getSelectedItem().equals("Efectivo"))
        {
            //Coloca en blanco la referencia y deshabilita
            txtReferencia.setText(null);
            txtReferencia.setEnabled(false);
            txtRecibida.setEnabled(true);
        }
        else
        {
            //Coloca la cantidad a cobrar exacta
            txtRecibida.setText(lblCobrarView.getText());
            txtReferencia.setEnabled(true);
            lblCambioView.setText("0.00");
            txtRecibida.setEnabled(false);
        }
    }//GEN-LAST:event_cboFormaPagoActionPerformed

    private void lblCambioViewPropertyChange(java.beans.PropertyChangeEvent evt) {//GEN-FIRST:event_lblCambioViewPropertyChange
        // TODO add your handling code here:
        String sEntero;
        String sDecimal;
        int iLongitud;
        int iEntero;
        
//Obtengo la longitud
        iLongitud = lblCambioView.getText().length();
       
        //Obtengo la parte entera
        sEntero = lblCambioView.getText().substring(0,iLongitud-3);
        iEntero = Integer.valueOf(sEntero);
        
        //Obtengo la parte decimal
        sDecimal = lblCambioView.getText().substring(iLongitud-2)+"/100 M.N.";
        lblCambioLetras.setText(oFunc.getStringOfNumber(iEntero)+ " PESOS "+sDecimal);
    }//GEN-LAST:event_lblCambioViewPropertyChange

