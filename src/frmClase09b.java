    private void sbCalculaCambio(){
        double douPagar;
        double douRecibida;
        double douCambio;
        
        if (! lblCobrarView.getText().isEmpty() && ! txtRecibida.getText().isEmpty())
        {
         //Obtengo la cantidad a pagar
            douPagar = Double.valueOf(lblCobrarView.getText());
            //Obtengo la Cantidad Recibida
            douRecibida = Double.valueOf(txtRecibida.getText());
            
            //Calculo la diferencia hasta que sea mayor o igual a la cantidad recibida
            if (douRecibida >= douPagar)
            {
                //Calculo el cambio
                douCambio = douRecibida-douPagar;
            //Coloca el cambio
                lblCambioView.setText(String.format("%5.2f,",douCambio));
            }
            else
                //Coloca el cambio en 0
                lblCambioView.setText("0.00");
        }
    }
    
    private boolean fnBoolCobroCorrecto(){
        //Variable para el resultado
        boolean bResult = true;
        
        //Si es efectivo
        if (cboFormaPago.getSelectedIndex()==0)
        {
            if (lblCambioView.getText().equals("0.00"))
            {
                oFunc.SubSistemaMensaje("Error en Cantidad Recibida");
                txtRecibida.requestFocus();
                bResult=false;
            }
        }
        else
            //Si es Tarjeta de Credito
            if (txtReferencia.getText().trim().isEmpty())
            {
                oFunc.SubSistemaMensaje("Capture referencia");
                txtReferencia.requestFocus();
                bResult=false;
            }
        return bResult;
    }
